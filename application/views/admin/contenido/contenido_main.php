    								<h2>Mision del Departamento</h2>
                                      El Departamento de Ingeniería Informática y Ciencias de la Computación pertenece a la Facultad de Ingeniería de la Universidad de Atacama y
                                       comparte con ellas su filosofia centrada en la ética, cultura, desarrollo sustentable y equidad social.
                                      Nuestra misión consiste en cultivar las Ciencias de la Computación y la Ingeniería Informática, a través de la
                                      investigación, divulgación, aplicación y formación de profesionales, contribuyendo de esta forma, al progreso de nuestro país.
                                      <h2>Vision del Departamento</h2>
                                       Ser una unidad académica de excelencia en investigación, docencia y extensión, reconocida a nivel nacional e 
                                       internacional en el ámbito de las ciencias de la computación e informática.
